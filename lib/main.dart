import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:firebase_core/firebase_core.dart';
import 'package:provider/provider.dart';
import 'package:sharemeal/screens/login_screen.dart';
import 'firebase_options.dart';        // auto-generated by flutterfire configure
import 'models/app_state.dart';
import 'screens/auth_wrapper.dart';    // our new smart first screen
import 'constants/app_theme.dart';

void main() async {
  // Ensures Flutter is ready before we do anything
  WidgetsFlutterBinding.ensureInitialized();

  // Initialize Firebase — MUST happen before runApp()
  // DefaultFirebaseOptions.currentPlatform automatically picks
  // the right config for Android / iOS / Web
  await Firebase.initializeApp(
    options: DefaultFirebaseOptions.currentPlatform,
  );

  // Status bar styling (same as before)
  SystemChrome.setSystemUIOverlayStyle(const SystemUiOverlayStyle(
    statusBarColor:          Colors.transparent,
    statusBarIconBrightness: Brightness.light,
    statusBarBrightness:     Brightness.dark,
  ));

  runApp(
    ChangeNotifierProvider(
      create: (_) => AppState(),
      child: const ShareMealApp(),
    ),
  );
}

class ShareMealApp extends StatelessWidget {
  const ShareMealApp({super.key});

  @override
  Widget build(BuildContext context) {
    final appState = Provider.of<AppState>(context);

    return MaterialApp(
      title:                      'ShareMeal',
      debugShowCheckedModeBanner: false,
      theme:                      AppTheme.light,
      darkTheme:                  AppTheme.dark,
      themeMode: appState.isDarkMode ? ThemeMode.dark : ThemeMode.light,
      home: LoginScreen(),   // 👈 changed from LoginScreen to AuthWrapper
    );
  }
}